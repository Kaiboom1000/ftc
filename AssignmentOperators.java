public class AssignmentOperators {
    public static void main(String[] args) {
        int a = 5, b = 20, c = 1;
 
        a += 10;  
        b -= a;  
        c *= b;    
 
        System.out.println("a = " + a);  
        System.out.println("b = " + b);  
        System.out.println("c = " + c);  
    }
}